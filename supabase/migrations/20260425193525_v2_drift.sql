    users.email AS content,
    'User Profile'::text AS category,
    (users.user_id)::text AS id,
    concat_ws(' '::text, users.first_name, users.last_name) AS names,
    to_tsvector('english'::regconfig, concat_ws(' '::text, users.first_name, users.last_name, users.email)) AS tsv
   FROM public.users;


