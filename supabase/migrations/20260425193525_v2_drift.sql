CREATE OR REPLACE FUNCTION public.get_collaborators(current_user_id uuid)
 RETURNS TABLE(profile_user_id uuid, cosine_similarity double precision, first_name text, last_name text, profile_pic_path text, occupation text, workplace text)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$BEGIN
  RETURN QUERY
  WITH overlap AS (
    SELECT pt2.profile_user_id,
      SUM(pt1.weight * pt2.weight) AS dot_product
    FROM profile_tags pt1
    JOIN profile_tags pt2 ON pt1.tag_id = pt2.tag_id
    WHERE pt1.profile_user_id = current_user_id
      AND pt2.profile_user_id != current_user_id
    GROUP BY pt2.profile_user_id
  ),
  magnitudes AS (
    SELECT pt.profile_user_id,
      SQRT(SUM(pt.weight * pt.weight)) AS magnitude
    FROM profile_tags pt
    GROUP BY pt.profile_user_id
  )
  SELECT o.profile_user_id,
    o.dot_product / (m1.magnitude * m2.magnitude) AS cosine_similarity,
    p.first_name,
    p.last_name,
    p.header_pic_path,
    p.occupation,
    p.workplace
  FROM overlap o
  JOIN magnitudes m1 ON m1.profile_user_id = current_user_id
  JOIN magnitudes m2 ON m2.profile_user_id = o.profile_user_id
  JOIN profile p ON p.user_id = o.profile_user_id
  ORDER BY cosine_similarity DESC;
END;$function$
;

CREATE OR REPLACE FUNCTION public.scale_vector(v extensions.vector, s double precision)
 RETURNS extensions.vector
 LANGUAGE plpgsql
AS $function$
BEGIN
  RETURN (SELECT array_agg(x * s)::vector FROM unnest(v::float4[]) AS x);
END;
$function$
