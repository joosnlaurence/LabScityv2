CREATE OR REPLACE FUNCTION public.handle_new_message()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
BEGIN
  -- Insert notifications for all participants except the sender, 
  -- checking both global preferences AND specific muted conversations
  INSERT INTO public.notifications (user_id, type, title, content, link)
  SELECT 
    cp.user_id, 
    'new_message', 
    'New Message', 
    'You received a new message.', 
    '/chat/' || NEW.conversation_id
  FROM public.conversation_participants cp
  LEFT JOIN public.notification_preferences np 
    ON np.user_id = cp.user_id AND np.notification_type = 'new_message'
  WHERE cp.conversation_id = NEW.conversation_id 
    AND cp.user_id != NEW.sender_id
    AND (np.is_enabled IS NULL OR np.is_enabled = true)
    -- NEW LOGIC: Exclude users who have this specific conversation muted
    AND NOT EXISTS (
      SELECT 1 FROM public.muted_items mi 
      WHERE mi.user_id = cp.user_id 
        AND mi.item_id = NEW.conversation_id 
        AND mi.item_type = 'conversation'
    );
  
  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.invite_user_to_group(p_group_id bigint, p_target_user_id uuid)
