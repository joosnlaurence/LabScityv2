    to_tsvector('english'::regconfig, concat_ws(' '::text, articles.source, articles.researchers)) AS tsv
   FROM public.articles
UNION ALL
 SELECT 'group'::text AS content_type,
    groups.description AS content,
    'user-defined group'::text AS category,
    (groups.group_id)::text AS id,
    groups.name AS names,
    to_tsvector('english'::regconfig, concat_ws(' '::text, groups.description, groups.name)) AS tsv
   FROM public.groups
UNION ALL
